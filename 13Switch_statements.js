//Switch statements
const grade='C';
switch(grade){
    case 'A':
        console.log('you got A');
        break;
    case 'B':
        console.log('you got B');
        break;
    case 'C':
        console.log('you got C');
        break;
    case 'D':
        console.log('you got D');
        break;
    case 'E':
        console.log('you got E');
        break;
    case 'F':
        console.log('you got F');
        break;
    default:
        console.log('invalid grade')
}